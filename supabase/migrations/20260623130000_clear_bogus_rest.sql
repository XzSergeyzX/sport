-- Чистка «бешеного» времени отдыха: баг авто-отдыха писал rest_sec = (сейчас − последний подход)
-- при переотметке подхода в УЖЕ завершённой тренировке («Відновити» старой сессии) → отдых в недели.
-- Клиентский фикс (onToggleDone считает разрыв только в живой тренировке) убирает причину;
-- здесь обнуляем уже записанный мусор. Порог 1 ч — реального отдыха между подходами столько не бывает,
-- так что глобально безопасно (затронет только сеты, пострадавшие от этого бага). На свежей базе — 0 строк.

update public.sets
set rest_sec = null
where rest_sec is not null
  and rest_sec > 3600;
